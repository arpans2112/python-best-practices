fruits_tuple = ("apple", "banana", "strawberry", "blueberry","apple")
# len() : returns the total number of elements in the fruits
print(len(fruits_tuple))

# count(): Tells the number of given item as an argument.
print(fruits_tuple.count("apple"))

# index(): Searches the tuple for a specified value and returns the position of where
# it was found at first from the left
print(fruits_tuple.index("apple"))


